    private void refreshGui()
    {
        if (myRadio.getEncendido()){
//            jLabel3.setText("R");
            if (myRadio.getFrecuencia())
            {
                jLabel1.setText(String.valueOf(myRadio.getEstacionAM()));
                jLabel2.setText("AM");
                
            }
            else 
            {
                jLabel1.setText(String.valueOf(myRadio.getEstacionFM()));
                jLabel2.setText("FM");
            }
        }
        else {
            jLabel1.setText("-");
            jLabel2.setText("-");
            jLabel3.setText("-");
        }
    }
